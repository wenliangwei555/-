package cn.baisee.oa.constants;
/**
 * 缓存Key的管理
 */
public enum CacheKeyEnum {
	
	//常量缓存Key-value
	CONSTAN_CACHE_KEY_VALUE("constans.by.key.value"),
	
	//FTP服务器缓存
	FTP_SERVER_LIST("ftp.server.list"),
	FTP_SERVER_ITEM("ftp.server.item"),
	
	//登录缓存
	LOGIN_IMAGE_CODE("login.code."),
	LOGIN("login."),
	//缓存菜单
	MENU_ALL("menu.all"),
	MENU_USER_INFO("menu.userinfo"),
	
	// 试题缓存
	CURRICULUM("curriculum"),
	
	//学费缓存
	TUITION("tuition."),
	;
	
	//缓存Key
	private String cacheKey;

	private CacheKeyEnum(String cacheKey) {
		this.cacheKey = cacheKey;
	}

	public String getCacheKey() {
		return cacheKey;
	}

	public void setCacheKey(String cacheKey) {
		this.cacheKey = cacheKey;
	}
	
}
