package cn.baisee.oa.model.student;

public class BaiseeStudentReturn {
	//主键
	private String rtId;
	//规则名
	private String rName;
	//更新时间
	private String updateTime;
	//创建人
	private String createUser;
	//返费规则
	private BaiseeStudentReturnRule returnRule;
	public BaiseeStudentReturnRule getReturnRule() {
		return returnRule;
	}
	public void setReturnRule(BaiseeStudentReturnRule returnRule) {
		this.returnRule = returnRule;
	}
	public String getRtId() {
		return rtId;
	}
	public void setRtId(String rtId) {
		this.rtId = rtId;
	}
	public String getrName() {
		return rName;
	}
	public void setrName(String rName) {
		this.rName = rName;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getCreateUser() {
		return createUser;
	}
	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}
	
}
