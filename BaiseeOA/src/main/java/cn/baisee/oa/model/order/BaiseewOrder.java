package cn.baisee.oa.model.order;

public class BaiseewOrder {
	
	private Integer id;//本表id
	private String openId;//微信ID，防止重复提交
	private String orderName;//姓名
	private String orderPhone;//手机号
	private String orderQQ;//QQ
	private String orderOld;//是否已联系
	private String orderContent;//备注
	private String oderDate;//创建时间
	private String orClassEs;//0 高中课程 1初中课程
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getOrderName() {
		return orderName;
	}
	public void setOrderName(String orderName) {
		this.orderName = orderName;
	}
	public String getOrderPhone() {
		return orderPhone;
	}
	public void setOrderPhone(String orderPhone) {
		this.orderPhone = orderPhone;
	}
	public String getOrderQQ() {
		return orderQQ;
	}
	public void setOrderQQ(String orderQQ) {
		this.orderQQ = orderQQ;
	}
	public String getOrderOld() {
		return orderOld;
	}
	public void setOrderOld(String orderOld) {
		this.orderOld = orderOld;
	}
	public String getOrderContent() {
		return orderContent;
	}
	public void setOrderContent(String orderContent) {
		this.orderContent = orderContent;
	}
	public String getOderDate() {
		return oderDate;
	}
	public void setOderDate(String oderDate) {
		this.oderDate = oderDate;
	}
	public String getOrClassEs() {
		return orClassEs;
	}
	public void setOrClassEs(String orClassEs) {
		this.orClassEs = orClassEs;
	}
	


}
