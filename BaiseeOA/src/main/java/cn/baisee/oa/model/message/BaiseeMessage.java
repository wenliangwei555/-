package cn.baisee.oa.model.message;

import java.util.Date;

/**
 * 消息
 * @author apple
 *
 */
public class BaiseeMessage {

	private Long id;
	/**
	 * 模板ID
	 */
	private Long templateId;
	/**
	 * 标题
	 */
	private String title;
	/**
	 * 内容
	 */
	private String content;
	/**
	 * 连接地址（微信模板消息）
	 */
	private String linkUrl;

	private String type;
	/**
	 * 发送人
	 */
	private String fromUserId;
	/**
	 * 发送人姓名
	 */
	private String fromUserName;
	/**
	 * 接收人
	 */
	private String receiveUserId;
	/**
	 * 接收人姓名
	 */
	private String receiveUserName;
	/**
	 * 是否已读： 0否、1是
	 */
	private String isReader;
	/**
	 * 是否删除： 0否、1是
	 */
	private String isDelete;
	/**
	 * 创建时间
	 */
	private Date createTime;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getTemplateId() {
		return templateId;
	}

	public void setTemplateId(Long templateId) {
		this.templateId = templateId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getLinkUrl() {
		return linkUrl;
	}

	public void setLinkUrl(String linkUrl) {
		this.linkUrl = linkUrl;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getFromUserId() {
		return fromUserId;
	}

	public void setFromUserId(String fromUserId) {
		this.fromUserId = fromUserId;
	}

	public String getFromUserName() {
		return fromUserName;
	}

	public void setFromUserName(String fromUserName) {
		this.fromUserName = fromUserName;
	}

	public String getReceiveUserId() {
		return receiveUserId;
	}

	public void setReceiveUserId(String receiveUserId) {
		this.receiveUserId = receiveUserId;
	}

	public String getReceiveUserName() {
		return receiveUserName;
	}

	public void setReceiveUserName(String receiveUserName) {
		this.receiveUserName = receiveUserName;
	}

	public String getIsReader() {
		return isReader;
	}

	public void setIsReader(String isReader) {
		this.isReader = isReader;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getIsDelete() {
		return isDelete;
	}

	public void setIsDelete(String isDelete) {
		this.isDelete = isDelete;
	}

	@Override
	public String toString() {
		return "BaiseeMessage [id=" + id + ", templateId=" + templateId + ", title=" + title + ", content=" + content
				+ ", linkUrl=" + linkUrl + ", type=" + type + ", fromUserId=" + fromUserId + ", fromUserName="
				+ fromUserName + ", receiveUserId=" + receiveUserId + ", receiveUserName=" + receiveUserName
				+ ", isReader=" + isReader + ", isDelete=" + isDelete + ", createTime=" + createTime + "]";
	}

	

}
