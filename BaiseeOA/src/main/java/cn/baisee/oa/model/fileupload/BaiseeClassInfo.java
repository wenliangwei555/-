package cn.baisee.oa.model.fileupload;

/**
 * 上传者信息
 * @author G
 *
 */
public class BaiseeClassInfo {
	private String id;    //主键
	private String typeId; //科目编号
	private String lecturerId;  //上传者编号
	private String lecturerName; //上传者姓名
	private String updateTime; //更新时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTypeId() {
		return typeId;
	}
	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}
	public String getLecturerId() {
		return lecturerId;
	}
	public void setLecturerId(String lecturerId) {
		this.lecturerId = lecturerId;
	}
	public String getLecturerName() {
		return lecturerName;
	}
	public void setLecturerName(String lecturerName) {
		this.lecturerName = lecturerName;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "BaiseeClassInfo [id=" + id + ", typeId=" + typeId + ", lecturerId=" + lecturerId + ", lecturerName="
				+ lecturerName + ", updateTime=" + updateTime + "]";
	}
	
}
