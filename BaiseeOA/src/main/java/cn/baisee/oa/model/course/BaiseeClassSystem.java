package cn.baisee.oa.model.course;

import java.io.Serializable;

/**
 * 班级的课制
 * @author Administrator
 *
 */
public class BaiseeClassSystem implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String cid;//班级id；
	private String tsId;//某节课的id
	private String csId;//课制id
	private String ctId;//时间段id
	private String weekCount;//星期总数
	private String timeStage;//一天的课数
	private String endTime;//一节课的结束时间
	private String courseOrder;//课程排序
	private String beginTime;//一节课的开始时间
	
	
	
	
	public String getCsId() {
		return csId;
	}
	public void setCsId(String csId) {
		this.csId = csId;
	}

	public String getCtId() {
		return ctId;
	}
	public void setCtId(String ctId) {
		this.ctId = ctId;
	}
	public String getTsId() {
		return tsId;
	}
	public void setTsId(String tsId) {
		this.tsId = tsId;
	}
	public String getBeginTime() {
		return beginTime;
	}
	public void setBeginTime(String beginTime) {
		this.beginTime = beginTime;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getWeekCount() {
		return weekCount;
	}
	public void setWeekCount(String weekCount) {
		this.weekCount = weekCount;
	}
	public String getTimeStage() {
		return timeStage;
	}
	public void setTimeStage(String timeStage) {
		this.timeStage = timeStage;
	}
	
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getCourseOrder() {
		return courseOrder;
	}
	public void setCourseOrder(String courseOrder) {
		this.courseOrder = courseOrder;
	};
	
	
}
