package cn.baisee.oa.model.fileupload;

/**
 * 科目实体
 * @author G
 *
 */
public class BaiseeClassType {
	private String id;    //主键
	private String type;  //部门类型1；高中 0：初中
	private String name;  //科目名称
	private String pid;   //父节点编号
	private String orderNum;   //排序编号
	private String updateTime; //更新时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	@Override
	public String toString() {
		return "BaiseeClassType [id=" + id + ", type=" + type + ", name=" + name + ", pid=" + pid + ", orderNum="
				+ orderNum + ", updateTime=" + updateTime + "]";
	}
	
}
