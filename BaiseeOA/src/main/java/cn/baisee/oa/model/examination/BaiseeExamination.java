package cn.baisee.oa.model.examination;
/**
 * 考试表
 * 
 * @author jxx
 *
 */
public class BaiseeExamination {
	private String eid;		//主键
	private String tid;		//试卷Id
	private String cid;		//班级Id
	private String createTs;//创建时间
	private String teacher; //监考老师
	private String examinationPlace;//考试场地
	private String sort;	//排序
	private String updateTs;//更新时间
	private String endTime;//结束时间
	private String startTime;// 开始时间
	private String examinationStatic;// 考试状态 0未开始考试  1正在考试   2 已结束考试
	private String term; //考试期数
	private String stateOfOwnership; //考试所属状态 ( 0初中 /  1高中)
	
	public String getStateOfOwnership() {
		return stateOfOwnership;
	}
	public void setStateOfOwnership(String stateOfOwnership) {
		this.stateOfOwnership = stateOfOwnership;
	}
	public String getTerm() {
		return term;
	}
	public void setTerm(String term) {
		this.term = term;
	}
	public String getExaminationStatic() {
		return examinationStatic;
	}
	public void setExaminationStatic(String examinationStatic) {
		this.examinationStatic = examinationStatic;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getUpdateTs() {
		return updateTs;
	}
	public void setUpdateTs(String updateTs) {
		this.updateTs = updateTs;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public String getEid() {
		return eid;
	}
	public void setEid(String eid) {
		this.eid = eid;
	}
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCreateTs() {
		return createTs;
	}
	public void setCreateTs(String createTs) {
		this.createTs = createTs;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public String getExaminationPlace() {
		return examinationPlace;
	}
	public void setExaminationPlace(String examinationPlace) {
		this.examinationPlace = examinationPlace;
	}
	@Override
	public String toString() {
		return "baiseeExamination [eid=" + eid + ", tid=" + tid + ", cid=" + cid + ", createTs=" + createTs
				+ ", teacher=" + teacher + ", examinationPlace=" + examinationPlace + "]";
	}
	
}
