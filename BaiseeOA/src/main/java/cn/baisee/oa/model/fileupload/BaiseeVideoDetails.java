package cn.baisee.oa.model.fileupload;

/**
 * 视频详情实体类
 * @author G
 *
 */
public class BaiseeVideoDetails {
	private String id;     //视频详情主键
	private String name;     //科目名称
	private String videoName;  //视频名称
	private String videoUrl;  //视屏播放路径
	private String lecturerName;  //视频教学老师名称
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getVideoUrl() {
		return videoUrl;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoUrl = videoUrl;
	}
	public String getLecturerName() {
		return lecturerName;
	}
	public void setLecturerName(String lecturerName) {
		this.lecturerName = lecturerName;
	}
	@Override
	public String toString() {
		return "BaiseeVideoDetails [id=" + id + ", name=" + name + ", videoName=" + videoName + ", videoUrl=" + videoUrl
				+ ", lecturerName=" + lecturerName + "]";
	}
	
}
