package cn.baisee.oa.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 考勤表
 */
public class BaiseeAttence implements Serializable {

	private String id;//ID
	private String userId;//员工Id
	private Date att_time;//打卡时间
	private String address;//打卡地点
	private String distance;//距离
	private String content;//描述
	
	private String date;//日期
	private String eName;//员工姓名
	private String details;//打卡描述
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public Date getAtt_time() {
		return att_time;
	}
	public void setAtt_time(Date att_time) {
		this.att_time = att_time;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDistance() {
		return distance;
	}
	public void setDistance(String distance) {
		this.distance = distance;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String geteName() {
		return eName;
	}
	public void seteName(String eName) {
		this.eName = eName;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}

}
