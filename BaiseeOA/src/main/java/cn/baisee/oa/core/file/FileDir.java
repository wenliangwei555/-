package cn.baisee.oa.core.file;

public enum FileDir {
	/**
	 * 员工的头像文件
	 */
	C_PICTURE_DIR("emp"),
	
	F_PICTURE_DIR("file"),

	S_PICTURE_DIR("student"),
	
	R_PICTURE_DIR("repair");//报修任务的图片
	
	private String itemKey;

	FileDir(String itemKey) {
		this.itemKey = itemKey;
	}

	public String getItemKey() {
		return itemKey;
	}
}
